class Color:
    __fields__ = [
        "m_a"
      , "m_b"
      , "m_g"
      , "m_r"
    ]
    
    def __init__(self, data):
        self._data = data
    
    def r():
        return self._data['m_r']
    
    def g():
        return self._data['m_g']
    
    def b():
        return self._data['m_b']
        
    def a():
        return self._data['m_a']
    
